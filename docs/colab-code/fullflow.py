    print("="*80)
    for col in ["Test Loss", "Accuracy", "Macro Precision", "Macro Recall", "Macro F1-Score"]:
        print(f"{col:<20}: {mean_metrics[col]:.4f}")

    return df_metrics, mean_metrics, std_metrics

