        axes[0].plot(history['val_loss'], label='Val Loss', color='red')
        axes[0].set_title(f'Run {i+1}: Loss Curve', fontweight='bold')
        axes[0].set_xlabel('Epochs')
        axes[0].set_ylabel('Loss')
        axes[0].legend()
