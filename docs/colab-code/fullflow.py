from torch_geometric.nn import GATConv, GAE
import torch.nn.functional as F
import torch

class GATEncoder(torch.nn.Module):
    def __init__(self, in_channels, out_channels):
        super().__init__()

        self.conv1 = GATConv(
            in_channels,
            32,
            heads=4,
            dropout=0.3,
            edge_dim=1  # edge_dim=1: Để model biết có trọng số cạnh 1 chiều
        )

        # Input channel là 32 * 4 (do 4 heads ở layer 1)
        self.conv2 = GATConv(
            32 * 4,
            out_channels,
            heads=1,
            concat=False,
            dropout=0.3,
            edge_dim=1  # edge_dim=1 để tiếp tục sử dụng trọng số cạnh ở tầng này
        )

    def forward(self, x, edge_index, edge_attr):
        x = self.conv1(x, edge_index, edge_attr=edge_attr)
        x = F.elu(x)
        x = F.dropout(x, p=0.3, training=self.training)
        x = self.conv2(x, edge_index, edge_attr=edge_attr)
        return x

in_channels = data.num_features
out_channels = 16

device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')

def count_parameters(model):
    return sum(p.numel() for p in model.parameters() if p.requires_grad)

from torch_geometric.nn import GAE

model = GAE(GATEncoder(in_channels, out_channels))

model = model.to(device)
data = data.to(device)

print(f"Tổng số tham số: {count_parameters(model)}")
print(model)

optimizer = torch.optim.Adam(model.parameters(), lr=0.005)
nums_epochs = 1000

# CẤU HÌNH EARLY STOPPING
patience = 100          # Số epoch chấp nhận không cải thiện trước khi dừng (thường là 20-50)
min_delta = 0.001      # Ngưỡng cải thiện tối thiểu (để tránh việc giảm li ti 0.000001 mà vẫn tính là giảm)
patience_counter = 0   # Biến đếm số lần không cải thiện liên tiếp

loss_history = []
best = dict(best_epoch=0, best_loss=float('inf'))  # {best_epoch: ..., best_loss: ...}

model.train()
for epoch in range(nums_epochs):
    optimizer.zero_grad()

    z = model.encode(data.x, data.edge_index, edge_attr=data.edge_attr)

    # Calculate Loss (Reconstruction Loss)
    loss = model.recon_loss(z, data.edge_index)

    loss.backward()
    optimizer.step()

    current_loss = loss.item()
    loss_history.append(current_loss)

    # LOGIC EARLY STOPPING & CHECKPOINT
    if current_loss < (best['best_loss'] - min_delta):
        best['best_loss'] = current_loss
        best['best_epoch'] = epoch + 1
        patience_counter = 0 # Reset bộ đếm vì đã tìm thấy kỷ lục mới

        # Lưu model tốt nhất
        best_z = z.detach().cpu().numpy() # Lưu embedding tốt nhất
    else:
        patience_counter += 1

    if (epoch + 1) % 25 == 0 or (epoch + 1) == nums_epochs:
        print(f"Epoch {epoch+1:03d}/{nums_epochs} | Loss: {current_loss:.6f} | Best: {best['best_loss']:.6f} | Patience: {patience_counter}/{patience}")

    # KÍCH HOẠT DỪNG SỚM
    if patience_counter >= patience:
        print(f"\nEARLY STOP")
        print(f"   Đã dừng tại Epoch {epoch+1} vì Loss không giảm trong {patience} epochs liên tiếp.")
        print(f"   Best Loss đạt được: {best['best_loss']:.6f}")
        break

print("\nQuá trình huấn luyện kết thúc.")
print(f"   Best Loss đạt được: {best['best_loss']:.6f} đạt tại {best['best_epoch']}")

