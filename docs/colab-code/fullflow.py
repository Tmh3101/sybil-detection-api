PCT_CO_OWNER_THRESHOLD = 0.1
PCT_FUZZY_HANDLE_THRESHOLD = 0.50
PCT_SIMILARITY_THRESHOLD = 0.60
STD_CREATION_HOURS_THRESHOLD = 0.5
PCT_SOCIAL_THRESHOLD = 0.20
AVG_TRUST_THRESHOLD = 8
FALLBACK_LOW_TRUST_THRESHOLD = 5 # New threshold for fallback rule

def calculate_sybil_risk_score(stats):
    """
    Hệ thống chấm điểm rủi ro cộng dồn (Additive Risk Scoring)
    Điểm rủi ro (Risk Score) dao động từ 0 đến 100.
    """
    score = 0
    reasons = []

    # 1. Tiêu chí Đồng sở hữu (Co-owner) - Rất nghiêm trọng
    if stats['pct_co_owner'] > PCT_CO_OWNER_THRESHOLD:
        score += 50
        reasons.append(f"Co-owner ({stats['pct_co_owner']:.1%}) [+50]")

    # 2. Tiêu chí Tương đồng Siêu dữ liệu (Similarity) - CHIA BẬC
    if stats['size'] > 2 and stats['pct_similarity'] >= PCT_SIMILARITY_THRESHOLD:
        score += 30
        reasons.append(f"High Similarity ({stats['pct_similarity']:.1%}) [+30]")

    # 3. Tiêu chí Khởi tạo hàng loạt (Batch Creation)
    # CHỈ ÁP DỤNG cho các cụm có nhiều hơn 1 tài khoản
    if stats['size'] > 1 and stats['std_creation_hours'] < STD_CREATION_HOURS_THRESHOLD:
        score += 15
        reasons.append(f"Batch Creation (< 2h std) [+15]")

    # 4. Tiêu chí Cú pháp tên (Fuzzy Handle)
    if stats['pct_fuzzy_handle'] >= PCT_FUZZY_HANDLE_THRESHOLD:
        score += 15
        reasons.append(f"Fuzzy Handle ({stats['pct_fuzzy_handle']:.1%}) [+15]")

    # 5. Tiêu chí Tương tác Xã hội thấp (Low Social)
    if stats['pct_social'] <= PCT_SOCIAL_THRESHOLD:
        score += 10
        reasons.append(f"Low Social ({stats['pct_social']:.1%}) [+10]")

    # 6. Tiêu chí Độ tin cậy (Trust Score)
    if stats['avg_trust'] <= FALLBACK_LOW_TRUST_THRESHOLD:
        score += 20
        reasons.append(f"Extremely Low Trust ({stats['avg_trust']:.2f}) [+20]")
    elif stats['avg_trust'] <= AVG_TRUST_THRESHOLD:
        score += 10
        reasons.append(f"Low Trust ({stats['avg_trust']:.2f}) [+10]")

    # Chuẩn hóa điểm số tối đa là 100
    risk_score = min(score, 100)

    # Phân lớp mờ (Fuzzy Labeling - 4 Classes)
    if risk_score < 20:
        label = "0_BENIGN"
    elif risk_score <= 50:
        label = "1_LOW_RISK"
    elif risk_score < 80:
        label = "2_HIGH_RISK"
    else:
        label = "3_MALICIOUS"

    if not reasons:
        reasons.append("Organic behavior")

    return risk_score, label, " | ".join(reasons)

