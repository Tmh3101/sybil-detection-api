def extract_universal_embeddings(gnn_model, data, device, model_path):
    print(f"📥 Đang nạp trọng số GNN từ: {model_path}")
    gnn_model.load_state_dict(torch.load(model_path, map_location=device))
    gnn_model.eval()

    extracted_features = {}
    def get_activation(name):
        def hook(model, input, output):
            extracted_features[name] = output.detach().cpu().numpy()
        return hook

    handle = gnn_model.conv2.register_forward_hook(get_activation('embeddings'))
    with torch.no_grad():
        gnn_model(data.x, data.edge_index, getattr(data, 'edge_attr', None))
    handle.remove()

    X_gnn = extracted_features['embeddings']
    y_labels = data.y.cpu().numpy()

    print(f"✅ Đã trích xuất Universal Embeddings: {X_gnn.shape}\n")
    return X_gnn, y_labels

