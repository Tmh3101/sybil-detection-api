# ==========================================
# 4. PNA CLASSIFIER (Principal Neighbourhood Aggregation)
# ==========================================
class PNAClassifier(torch.nn.Module):
    def __init__(self, in_channels, embedding_dim, deg, num_classes=2):
        super().__init__()
        aggregators = ['mean', 'min', 'max', 'std']
        scalers = ['identity', 'amplification', 'attenuation']

        self.conv1 = PNAConv(
            in_channels=in_channels, out_channels=32,
            aggregators=aggregators, scalers=scalers, deg=deg,
            edge_dim=1, towers=1, pre_layers=1, post_layers=1
        )

        self.conv2 = PNAConv(
            in_channels=32, out_channels=embedding_dim,
            aggregators=aggregators, scalers=scalers, deg=deg,
            edge_dim=1, towers=1, pre_layers=1, post_layers=1
        )

        self.classifier = nn.Linear(embedding_dim, num_classes)

    def forward(self, x, edge_index, edge_attr):
        # Tương tự GINE, PNA cần edge_attr 2D float
        if edge_attr is not None:
            if edge_attr.dim() == 1:
                edge_attr = edge_attr.view(-1, 1)
            edge_attr = edge_attr.float()

        x = self.conv1(x, edge_index, edge_attr=edge_attr)
        x = F.elu(x)
        x = F.dropout(x, p=0.3, training=self.training)

        x = self.conv2(x, edge_index, edge_attr=edge_attr)

        out = self.classifier(x)
        return out


