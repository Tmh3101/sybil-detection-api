def run_repeated_gnn_experiments(
    model_class, model_kwargs, data, device, class_weights,
    n_runs=5, epochs=1000, patience=100, lr=0.001, weight_decay=5e-4,
    class_names=['0_BENIGN', '1_LOW_RISK', '2_HIGH_RISK', '3_MALICIOUS'],
    save_path='best_gnn_model.pth' # <--- THÊM THAM SỐ LƯU ĐƯỜNG DẪN
):
    seeds = [42, 123, 999, 2024, 777, 888, 111, 222, 333, 444][:n_runs]

    all_metrics = []

    # Biến theo dõi Nhà vô địch toàn cục (Global Best)
    global_best_test_f1 = 0.0

    print("="*80)
    print(f"🚀 BẮT ĐẦU THỰC NGHIỆM: {model_class.__name__} ({n_runs} LẦN CHẠY)")
    print(f"💾 Mô hình tốt nhất sẽ được lưu tại: {save_path}")
    print("="*80)

    for i, seed in enumerate(seeds):
        set_seed(seed)

        model = model_class(**model_kwargs).to(device)
        optimizer = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=weight_decay)
        criterion = torch.nn.CrossEntropyLoss(weight=class_weights.to(device))

        best_val_f1 = 0
        best_epoch = 0
        patience_counter = 0
        best_weights = None
        history = {'train_loss': [], 'val_loss': [], 'val_f1': []}

        print(f"\n" + "-"*60)
        print(f"🔄 Đang chạy Lần {i+1}/{n_runs} (Seed: {seed})")
        print("-"*60)

        for epoch in range(epochs):
            model.train()
            optimizer.zero_grad()
            out = model(data.x, data.edge_index, data.edge_attr)
            train_loss = criterion(out[data.train_mask], data.y[data.train_mask])
            train_loss.backward()
            optimizer.step()

            model.eval()
            with torch.no_grad():
                out_val = model(data.x, data.edge_index, data.edge_attr)
                val_loss = criterion(out_val[data.val_mask], data.y[data.val_mask]).item()
                preds_val = out_val.argmax(dim=1)

                y_true_val = data.y[data.val_mask].cpu().numpy()
                y_pred_val = preds_val[data.val_mask].cpu().numpy()
                val_f1 = f1_score(y_true_val, y_pred_val, average='macro', zero_division=0)

            history['train_loss'].append(train_loss.item())
            history['val_loss'].append(val_loss)
            history['val_f1'].append(val_f1)

            # Early Stopping
            if val_f1 > best_val_f1:
                best_val_f1 = val_f1
                best_epoch = epoch + 1
                best_weights = {k: v.cpu() for k, v in model.state_dict().items()}
                patience_counter = 0
            else:
                patience_counter += 1

