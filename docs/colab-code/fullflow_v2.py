# Check close creation time using window and
def check_close_creation_time(df, seconds=5):
    pairs = []
    df_sorted = df.sort_values('created_on')
    times = df_sorted['created_on'].values
    indices = df_sorted.index.values

    window = pd.Timedelta(seconds=seconds)
    for i in range(len(df_sorted)):
        current_time = times[i]
        for j in range(i + 1, len(df_sorted)):
            time_diff = times[j] - current_time
            if time_diff > window:
                break
            # Trả về: u, v, số giây chênh lệch
            pairs.append((indices[i], indices[j], pd.Timedelta(time_diff).total_seconds()))
    return pairs

# check fuzzy handle (username) với rapidfuzz
def check_fuzzy_handle(df, threshold=80):
    pairs = []
    handles = df['handle'].astype(str).tolist()
    indices = df.index.tolist()

    n = len(df)
    for i in tqdm(range(n), desc="Fuzzy Handle"):
        for j in range(i + 1, n):
            h1, h2 = handles[i], handles[j]
            if len(h1) < 4 or len(h2) < 4:
                continue

            score = fuzz.ratio(h1, h2)
            if score >= threshold:
                # Trả về: u, v, điểm levenshtein ratio
                pairs.append((indices[i], indices[j], score))
    return pairs

# Check similarity bio with S-BERT model
def check_sim_bio(df, threshold=0.8):
    import torch
    import os

    pairs = []
    valid_indices = df[df['bio'].str.len() > 3].index.tolist()
    bios = df.loc[valid_indices, 'bio'].tolist()
    profile_ids = df.loc[valid_indices, 'profile_id'].tolist()

    if not bios:
        return []

    model = SentenceTransformer('all-MiniLM-L6-v2')
    embeddings = model.encode(bios, show_progress_bar=True)

    # Logic lưu lại embeddings vào bio_embeddings.pt
    save_emb_path = os.path.join(folder_path, "bio_embeddings.pt")
    bio_embs_dict = {
        str(pid): torch.tensor(emb, dtype=torch.float32)
        for pid, emb in zip(profile_ids, embeddings)
    }
    torch.save(bio_embs_dict, save_emb_path)
    print(f"✅ Đã lưu bio embeddings vào: {save_emb_path}")

    sim_matrix = cosine_similarity(embeddings)

    n = len(valid_indices)
    for i in range(n):
        for j in range(i + 1, n):
            score = sim_matrix[i][j]
            if score >= threshold:
                real_idx_i = valid_indices[i]
                real_idx_j = valid_indices[j]
                # Chuẩn hóa u < v để không bị lặp cạnh
                u, v = (real_idx_i, real_idx_j) if real_idx_i < real_idx_j else (real_idx_j, real_idx_i)
                # Trả về: u, v, điểm cosine
                pairs.append((u, v, float(score)))

    return pairs

