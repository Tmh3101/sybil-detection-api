class GINEClassifier(torch.nn.Module):
    def __init__(self, in_channels, embedding_dim, num_classes=2):
        super().__init__()
        # Layer 1 MLP
        mlp1 = nn.Sequential(
            nn.Linear(in_channels, 128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Linear(128, 128),
            nn.ReLU()
        )
        self.conv1 = GINEConv(nn=mlp1, train_eps=True, edge_dim=1)

        # Layer 2 MLP
        mlp2 = nn.Sequential(
            nn.Linear(128, 128),
            nn.BatchNorm1d(128),
            nn.ReLU(),
            nn.Linear(128, embedding_dim)
        )
        self.conv2 = GINEConv(nn=mlp2, train_eps=True, edge_dim=1)

        # Classification Head
        self.classifier = nn.Linear(embedding_dim, num_classes)

    def forward(self, x, edge_index, edge_attr):
        # Ép kiểu ma trận 2D và float cho edge_attr của GINE
        if edge_attr is not None:
            if edge_attr.dim() == 1:
                edge_attr = edge_attr.view(-1, 1)
            edge_attr = edge_attr.float()

        x = self.conv1(x, edge_index, edge_attr=edge_attr)
        x = F.dropout(x, p=0.1, training=self.training)
        x = self.conv2(x, edge_index, edge_attr=edge_attr)

        out = self.classifier(x)
        return out


