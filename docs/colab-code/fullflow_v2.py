    gnn_model = gnn_class(**gnn_kwargs).to(device)
    X_gnn, y_labels = extract_universal_embeddings(gnn_model, data, device, model_path)

    # Data Prep
    train_idx, test_idx = data.train_mask.cpu().numpy(), data.test_mask.cpu().numpy()
    X_train, y_train = X_gnn[train_idx], y_labels[train_idx]
