PCT_FUZZY_HANDLE_THRESHOLD = 0.50
STD_CREATION_HOURS_THRESHOLD = 0.5
PCT_SOCIAL_THRESHOLD = 0.15         # Ngưỡng thiếu hụt Social (<15% là bot)
AVG_TRUST_THRESHOLD = 10            # Cập nhật theo tài liệu (<=20 phạt 10đ)
FALLBACK_LOW_TRUST_THRESHOLD = 5    # Cập nhật theo tài liệu (<=5 phạt kịch khung 20đ)

def calculate_sybil_risk_score(stats):
    """
    Hệ thống chấm điểm rủi ro nhận thức ràng buộc (Constraint-Aware Scoring)
    """
    score = 0
    reasons = []

    # 1. Đồng sở hữu (Co-owner) [+50]
    if stats['pct_co_owner'] > PCT_CO_OWNER_THRESHOLD:
        score += 40
        reasons.append(f"Co-owner ({stats['pct_co_owner']:.1%}) [+40]")

    # 2. Tương đồng cấu trúc (Similarity) [+30]
    if stats['size'] > 2 and stats['pct_similarity'] >= PCT_SIMILARITY_THRESHOLD:
        score += 30
        reasons.append(f"High Similarity ({stats['pct_similarity']:.1%}) [+30]")

    # 3. Khởi tạo hàng loạt (Batch Creation) [+15]
    if stats['size'] > 1 and stats['std_creation_hours'] < STD_CREATION_HOURS_THRESHOLD:
        score += 15
        reasons.append(f"Batch Creation (< 0.5h std) [+15]")

    # 4. Cú pháp tên định danh (Fuzzy Handle) [+15]
    if stats['pct_fuzzy_handle'] >= PCT_FUZZY_HANDLE_THRESHOLD:
        score += 15
        reasons.append(f"Fuzzy Handle ({stats['pct_fuzzy_handle']:.1%}) [+15]")

    # 5. Tương tác xã hội thấp (Low Social) [+10]
    if stats['pct_social'] <= PCT_SOCIAL_THRESHOLD:
        score += 10
        reasons.append(f"Low Social ({stats['pct_social']:.1%}) [+10]")

    # 6. Cảnh báo Độ tin cậy (Trust Score) [+20 / +10]
    if stats['avg_trust'] <= FALLBACK_LOW_TRUST_THRESHOLD:
        score += 20
        reasons.append(f"Extremely Low Trust ({stats['avg_trust']:.2f}) [+20]")
    elif stats['avg_trust'] <= AVG_TRUST_THRESHOLD:
        score += 10
        reasons.append(f"Low Trust ({stats['avg_trust']:.2f}) [+10]")

    # Cắt cụt điểm số (Capping)
    risk_score = min(score, 100)

    # Phân lớp mờ (Fuzzy Labeling)
    if risk_score <= 20:     # Từ <20 đổi thành <=20 theo chuẩn doc
        label = "0_BENIGN"
    elif risk_score <= 50:
        label = "1_LOW_RISK"
    elif risk_score <= 80:   # Từ <80 đổi thành <=80 theo chuẩn doc
        label = "2_HIGH_RISK"
    else:
        label = "3_MALICIOUS"

    if not reasons:
        reasons.append("Organic behavior")

    return risk_score, label, " | ".join(reasons)

