def build_multidigraph(df_nodes, df_edges, df_final_labels):
    G = nx.MultiDiGraph()

    df_nodes = df_nodes.merge(df_final_labels[['profile_id', 'label', 'risk_score', 'reason']], on='profile_id', how='left')

    for _, row in df_nodes.iterrows():
        raw_id = str(row['profile_id']).strip().lower()
        G.add_node(
            raw_id,
            handle=str(row.get('handle', raw_id)),
            owned_by=str(row.get('owned_by', 'N/A')).strip().lower(),
            created_on=str(row.get('created_on', 'N/A')),
            trust_score=row.get('trust_score', 0.0),
            risk_score=row.get('risk_score', 0.0),
            label=row.get('label', 'N/A'),
            reason=row.get('reason', 'N/A')
        )

    for _, row in df_edges.iterrows():
        u = str(row['source']).strip().lower()
        v = str(row['target']).strip().lower()
        layer = str(row.get('layer', 'Interact'))
        e_type = str(row.get('type', 'N/A'))

        # Kiểm tra xem cả 2 node có tồn tại trong đồ thị không
        if G.has_node(u) and G.has_node(v):
            # Thêm thẳng cạnh vào đồ thị (MultiDiGraph tự động cấp key phân biệt cho các cạnh trùng)
            G.add_edge(u, v, layer=layer, e_type=e_type)

    print(f"Hoàn tất: {G.number_of_nodes()} nodes, {G.number_of_edges()} edges (bao gồm các cạnh song song).")
    return G

