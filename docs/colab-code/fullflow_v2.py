def plot_metric(ax, k_vals, y_vals, title, ylabel, metric_type, chosen_k, color):
    # Đường cơ sở (Base line)
    ax.plot(k_vals, y_vals, marker='o', linestyle='-', color=color, linewidth=2, markersize=5, zorder=2)

    if metric_type == 'max':
        best_idx = np.argmax(y_vals)
        best_label_prefix = 'Đỉnh cực đại'
    else:
        best_idx = np.argmin(y_vals)
        best_label_prefix = 'Đáy cực tiểu'

    best_k = k_vals[best_idx]
    best_val = y_vals[best_idx]

    chosen_idx = np.where(k_vals == chosen_k)[0][0]
    chosen_val = y_vals[chosen_idx]

    # KHI ĐIỂM CHỌN VÀ ĐIỂM TỐT NHẤT KHÁC NHAU
    if best_k != chosen_k:
        # Đánh dấu điểm Tốt nhất (Dấu X màu xám đen)
        ax.scatter(best_k, best_val, color='#34495e', marker='X', s=100, zorder=6,
                   label=f'{best_label_prefix}: K={best_k} ({best_val:.4f})')
        ax.axvline(x=best_k, color='#bdc3c7', linestyle='--', alpha=0.8, linewidth=1.5, zorder=1)

        # Đánh dấu điểm Chọn (Chấm tròn màu đỏ)
        ax.scatter(chosen_k, chosen_val, color='#e74c3c', marker='o', s=100, zorder=7,
                   label=f'Thực tế chọn: K={chosen_k} ({chosen_val:.4f})')
