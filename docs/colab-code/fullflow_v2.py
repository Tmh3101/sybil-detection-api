        with torch.no_grad():
            logits = model(data.x, data.edge_index, data.edge_attr)
            test_loss = criterion(logits[data.test_mask], data.y[data.test_mask]).item()
            preds = logits.argmax(dim=1)

        y_true = data.y[data.test_mask].cpu().numpy()
        y_pred = preds[data.test_mask].cpu().numpy()

        acc = accuracy_score(y_true, y_pred)
        precision = precision_score(y_true, y_pred, average='macro', zero_division=0)
        recall = recall_score(y_true, y_pred, average='macro', zero_division=0)
        f1 = f1_score(y_true, y_pred, average='macro', zero_division=0)
        cm = confusion_matrix(y_true, y_pred)

        all_metrics.append({
            "Run": i + 1,
            "Test Loss": test_loss,
            "Accuracy": acc,
            "Macro Precision": precision,
            "Macro Recall": recall,
            "Macro F1-Score": f1,
            "Best Epoch": best_epoch
        })

        print(f"✅ Hoàn tất! Dừng tại Epoch: {best_epoch} | Test F1: {f1:.4f}")

        if f1 > global_best_test_f1:
            global_best_test_f1 = f1
            torch.save(model.state_dict(), save_path)
