def load_and_preprocess(csv_path):
    df = pd.read_csv(csv_path)

    # Parse Metadata để lấy Bio và Picture
    def parse_metadata(meta_str):
        try:
            meta = ast.literal_eval(meta_str)
            lens_data = meta.get('lens', {})
            bio = lens_data.get('bio', '')
            picture = lens_data.get('picture', '')

            if isinstance(picture, dict):
                picture = picture.get('item', '') or picture.get('url', '')
            return bio, picture

        except:
            return '', ''

    # Áp dụng parse
    # Zip 2 list lại để tạo cột nhanh hơn apply từng dòng
    meta_parsed = [parse_metadata(x) for x in df['metadata']]
    df['bio'] = [x[0] for x in meta_parsed]
    df['picture_url'] = [x[1] for x in meta_parsed]

    # Parse thời gian
    df['created_on'] = pd.to_datetime(df['created_on'])

    return df

