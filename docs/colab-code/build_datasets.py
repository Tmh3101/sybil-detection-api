# Check same avatar with picture (url) on ipfs
def check_same_avatar(df):
    pairs = [] # Chỉ xét những node có avatar (không rỗng)
    df_valid = df[df['picture_url'].str.len() > 5] # URL phải dài hơn 5 ký tự

